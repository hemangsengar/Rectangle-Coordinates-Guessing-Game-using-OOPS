import turtle
# Create a canvas instance
myturtle = turtle.Turtle()


myturtle.penup()

myturtle.goto(10,10)

myturtle.pendown()
myturtle.forward(100) #move 100 pixel
myturtle.left(90)# move 90 degree
myturtle.forward(100)
myturtle.left(90)
myturtle.forward(100)
myturtle.left(90)
myturtle.forward(100)


turtle.done()